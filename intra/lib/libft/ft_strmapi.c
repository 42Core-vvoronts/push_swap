#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*result;

	i = 0;
	result = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!result)
		return ((void *)0);
	while (*s)
	{
		*result++ = f(i, *s++);
		i++;
	}
	*result = '\0';
	return (result - i);
}
