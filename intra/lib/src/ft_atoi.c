#include "libft.h"

int	ft_atoi(const char *str)
{
	int		nstr;
	int		sign;

	sign = 1;
	nstr = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'
		|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nstr = 10 * nstr + (*str - '0');
		str++;
	}
	return (sign * nstr);
}
