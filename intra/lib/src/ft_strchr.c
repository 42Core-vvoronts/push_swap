#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	i;

	i = c;
	while (*str)
	{
		if (*str == i)
			return ((char *)str);
		str++;
	}
	if (*str == i)
		return ((char *)str);
	return (0);
}
